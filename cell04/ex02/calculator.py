print(f"{firstNum} - {SecNum} = {int(firstNum - SecNum)}")
print(f"{firstNum} / {SecNum} = {int(firstNum / SecNum)}")
print(f"{firstNum} * {SecNum} = {int(firstNum * SecNum)}")
